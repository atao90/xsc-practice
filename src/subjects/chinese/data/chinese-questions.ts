import type { Question } from '@/types'

type SeedQuestion = Omit<Question, 'id'>

function textQuestion(question_type: '拼音' | '诗词', grade: number, difficulty: number, stem: string, answer: string | string[], explanation: string, hint?: string): SeedQuestion {
  return { subject: '语文', question_type, grade, difficulty, content: { stem, hint }, answer: { type: 'text', value: answer }, explanation }
}

function choiceQuestion(question_type: '改错', grade: number, difficulty: number, sentence: string, options: string[], answer: string, explanation: string): SeedQuestion {
  return {
    subject: '语文', question_type, grade, difficulty,
    content: {
      stem: '下面句子有什么问题？请选出正确的修改方式。',
      sentence,
      options: options.map((text, index) => ({ key: String.fromCharCode(65 + index), text }))
    },
    answer: { type: 'choice', value: answer },
    explanation
  }
}

function readingQuestion(question_type: '课内阅读' | '课外阅读', grade: number, difficulty: number, passageTitle: string, passage: string, questions: { stem: string; options: string[]; answer: string }[], explanation: string): SeedQuestion {
  return {
    subject: '语文', question_type, grade, difficulty,
    content: {
      passageTitle,
      passage,
      questions: questions.map(q => ({
        stem: q.stem,
        options: q.options.map((text, index) => ({ key: String.fromCharCode(65 + index), text }))
      }))
    },
    answer: { type: 'reading', value: questions.map(q => q.answer) },
    explanation
  }
}

